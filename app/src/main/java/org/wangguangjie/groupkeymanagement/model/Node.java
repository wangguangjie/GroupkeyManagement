package org.wangguangjie.groupkeymanagement.model;

public class Node {

    private float x;
    private float y;

    public Node(float x1,float y1){
        x=x1;
        y=y1;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
